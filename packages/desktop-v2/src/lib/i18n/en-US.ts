/** ``Dict`` is the contract every locale file must satisfy: same set
 *  of keys as en-US, each mapping to a ``string`` (not a literal).
 *  We deliberately widen the value type so zh-CN can supply
 *  ``"今日"`` for a key whose English value is ``"Today"`` — without
 *  this both files would need ``as const`` and a structural type
 *  intersection, which TypeScript can't represent.
 *
 *  The set of keys is the literal ``keyof typeof en`` so missing
 *  translations still fail at build time (a zh-CN with the wrong
 *  set of keys won't compile). */
export type Dict = Record<keyof typeof en, string>;
